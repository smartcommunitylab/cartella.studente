package it.smartcommunitylab.csengine.extsource.infotn;

public class Certificazione {
	private String origin; 
	private String extid;
	private String origin_student;
	private String extid_student;
	private String date_certificate;
	private String title;
	private String description;
	private String language;
	private String judgment;
	private String judgmentmax;
	private String certifier;
	
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public String getExtid() {
		return extid;
	}
	public void setExtid(String extid) {
		this.extid = extid;
	}
	public String getOrigin_student() {
		return origin_student;
	}
	public void setOrigin_student(String origin_student) {
		this.origin_student = origin_student;
	}
	public String getExtid_student() {
		return extid_student;
	}
	public void setExtid_student(String extid_student) {
		this.extid_student = extid_student;
	}
	public String getDate_certificate() {
		return date_certificate;
	}
	public void setDate_certificate(String date_certificate) {
		this.date_certificate = date_certificate;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getLanguage() {
		return language;
	}
	public void setLanguage(String language) {
		this.language = language;
	}
	public String getJudgment() {
		return judgment;
	}
	public void setJudgment(String judgment) {
		this.judgment = judgment;
	}
	public String getJudgmentmax() {
		return judgmentmax;
	}
	public void setJudgmentmax(String judgmentmax) {
		this.judgmentmax = judgmentmax;
	}
	public String getCertifier() {
		return certifier;
	}
	public void setCertifier(String certifier) {
		this.certifier = certifier;
	}
}
