package it.smartcommunitylab.csengine.extsource.infotn;

public class Scuola {
	private String origin;
	private String extId;
	private String codiceMiur;
	private String codiceProvinciale;
	private String comune;
	private String name;
	private String email;
	private String address;
	private String latitude;
	private String longitude;
	private String phone;
	private String idplesso;
	
	public String getOrigin() {
		return origin;
	}
	public void setOrigin(String origin) {
		this.origin = origin;
	}
	public String getExtId() {
		return extId;
	}
	public void setExtId(String extId) {
		this.extId = extId;
	}
	public String getCodiceMiur() {
		return codiceMiur;
	}
	public void setCodiceMiur(String codiceMiur) {
		this.codiceMiur = codiceMiur;
	}
	public String getCodiceProvinciale() {
		return codiceProvinciale;
	}
	public void setCodiceProvinciale(String codiceProvinciale) {
		this.codiceProvinciale = codiceProvinciale;
	}
	public String getComune() {
		return comune;
	}
	public void setComune(String comune) {
		this.comune = comune;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getLatitude() {
		return latitude;
	}
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}
	public String getLongitude() {
		return longitude;
	}
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getIdplesso() {
		return idplesso;
	}
	public void setIdplesso(String idplesso) {
		this.idplesso = idplesso;
	}

}


