package it.smartcommunitylab.csengine.model.stats;

public class KeyValue {
	private String name;
	private Number value;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Number getValue() {
		return value;
	}
	public void setValue(Number value) {
		this.value = value;
	}
}
